using System;
using System.Collections.Generic;
using System.Text;

namespace Drill13Fuctions
{
    class CoolMath
    {
        public static int Plus100(int num)
        {
            num = num + 100;

            return num;
        }

        public static int Minus100(int num)
        {
            num = num - 100;

            return num;
        }

        public static int Times100(int num)
        {
            num = num * 100;

            return num;
        }


    }
}
